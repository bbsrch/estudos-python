# Condições v2
nome = input('Qual é o seu nome? ')
if nome == 'André':
    print('Que nome lindo, viril e másculo. Você deve ser uma pessoa maravilhosa!')
elif nome == 'Hanter':
    print('Uau, que exótico')
elif nome == 'Gustavo':
    print('Nossa que genérico')
else:
    print('Ai que nome feio :(')
print('Bem vindo(a), {}'.format(nome))